package de.hsog.utils;

/*Helperclass for Webscraping Mensaplan*/
public class Ingredient {

	private String name;
	
	public Ingredient() {
		this.name = "Baked Beans";
	}
	
	public Ingredient(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
